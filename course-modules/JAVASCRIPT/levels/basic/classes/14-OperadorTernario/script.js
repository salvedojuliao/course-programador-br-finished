// Operador Ternario (É parecido com IF and ELSE)

//Exemplo:
var idade = 17;
//Quero verificar se idade é maior ou igul a 18 e imprimir mensages (Pode e não pode);

idade >= 18 ? console.log("Pode comprar a bebida") : console.log("Não pode");
// idade >= 18 ? (A idade é maior do que 18?) 
// : Se não ele faz outra coisa;

// Outro exemplo (A graça de tudo)
var pode = idade >= 18 ? true : false;
console.log(pode);


var pode = idade >= 18 ? "Pode" : "Não pode";
console.log(pode);





