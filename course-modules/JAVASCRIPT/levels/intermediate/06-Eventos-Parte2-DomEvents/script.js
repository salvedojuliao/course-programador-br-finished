function onOver(elemento){ // Quando o mouse estiver em cima do elemento (div)
    elemento.style.backgroundColor = "red";
    //Quando o mouse estiver em cima elemento, ele modivida a cor de fundo para vermelho
}


function onOut(elemento){ // Quando o mouse sair de cima do elemento (div)
    elemento.style.backgroundColor = "cornflowerblue";
}